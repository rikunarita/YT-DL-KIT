const languages: Language[] = ['en', 'ja', 'zh']

interface TranslationContextProps {
  language: Language
  setLanguage: (lang: Language) => void
  t: (key: string, values?: Record<string, string | number>) => string
}

const TranslationContext = createContext<TranslationContextProps>({
  language: 'ja',
  setLanguage: () => {},
  t: (key: string) => key,
})

const getNestedValue = (obj: any, path: string[]): string | undefined => {
  return path.reduce((current, part) => {
    if (current && typeof current === 'object') {
      return current[part]
    }
    return undefined
  }, obj)
}

const formatText = (text: string, values?: Record<string, string | number>) => {
  if (!values) return text
  return Object.entries(values).reduce(
    (result, [key, value]) => result.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value)),
    text,
  )
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>('ja')

  useEffect(() => {
    const saved = window.localStorage.getItem('weavedlx-language') as Language | null
    if (saved && languages.includes(saved)) {
      setLanguageState(saved)
    } else {
      const browser = navigator.language.toLowerCase()
      if (browser.startsWith('zh')) setLanguageState('zh')
      else if (browser.startsWith('ja')) setLanguageState('ja')
      else setLanguageState('en')
    }
  }, [])

  const setLanguage = (lang: Language) => {
    if (!languages.includes(lang)) return
    window.localStorage.setItem('weavedlx-language', lang)
    setLanguageState(lang)
  }

  const t = (key: string, values?: Record<string, string | number>) => {
    const path = key.split('.')
    const message = getNestedValue(translations[language as Language], path) as string | undefined
    if (message) return formatText(message, values)
    const fallback = getNestedValue(translations.en, path) as string | undefined
    return fallback ? formatText(fallback, values) : key
  }

  const value = useMemo(() => ({ language, setLanguage, t }), [language])

  return (
    <TranslationContext.Provider value={value}>
      {children}
    </TranslationContext.Provider>
  )
}

export function useTranslation() {
  return useContext(TranslationContext)
}
